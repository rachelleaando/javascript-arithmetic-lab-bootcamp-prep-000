
// add
function add (a, b) {
  return a + b
}

//subtract

function subtract (a, b) {
  return a - b
}

//multiply

function multiply (a, b) {
  return a * b
}

//divide

function divide (a, b) {
  return a / b
}

//increment

function inc (n) {
  n++
  return n
}

//decrements

function dec (n) {
  n--
  return n
}

//parsing n

function makeInt (n) {
  return n
}

//parsing with base 10

function makeInt () {
  return makeInt(n, 10)
}
